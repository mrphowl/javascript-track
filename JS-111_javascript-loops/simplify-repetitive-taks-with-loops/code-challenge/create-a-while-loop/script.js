/** JS-111: JavaScript Loops
 * Code challenge: Create a While Loop
 */
let count = 0;
/**
 * Task 1: Create a while loop that logs the current value of count to 
 * the console 26 times. Use the count variable to track the number of times
 * the loop runs. Don't forget to increment count by one each time through
 * the loop.
 */
while( count < 26 ) {
  // Increment count then return the new value
  console.log(++count);
}
