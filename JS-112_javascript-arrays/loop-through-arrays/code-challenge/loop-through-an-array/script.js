/**
 * JS-112: JavaScript Arrays - Loop Through Arrays
 * 
 * Code challenge: Loop Through An Array
 */
const temperatures = [100, 90, 99, 80, 70, 65, 30, 10];
/*
Task 1: Use a for loop to iterate over the values inside the temperatures
array. Start from the first element (100) to the last (10). Inside the loop,
log the current array value to the console. Use i to represent the counter
variable.
*/
for ( let i = 0; i < temperatures.length; i++ ) {
  console.log(temperatures[i]);
}
